public class Calculator {

    public double slozhit(double a, double b){
        return a+b;
    }

    public double vichest(double a, double b){
        return a-b;
    }

    public double umnozhit(double a, double b){
        return a*b;
    }

    public double podelit(double a, double b){
        return a/b;
    }

}
